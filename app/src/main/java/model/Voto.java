package model;

public class Voto {

    private int id_voto;
    private int voto_blaco;
    private int voto_nulo;
    private int voto_boric;
    private int voto_kast;

    public int getId_voto() {
        return id_voto;
    }

    public void setId_voto(int id_voto) {
        this.id_voto = id_voto;
    }

    public int getVoto_blaco() {
        return voto_blaco;
    }

    public void setVoto_blaco(int voto_blaco) {
        this.voto_blaco = voto_blaco;
    }

    public int getVoto_nulo() {
        return voto_nulo;
    }

    public void setVoto_nulo(int voto_nulo) {
        this.voto_nulo = voto_nulo;
    }

    public int getVoto_boric() {
        return voto_boric;
    }

    public void setVoto_boric(int voto_boric) {
        this.voto_boric = voto_boric;
    }

    public int getVoto_kast() {
        return voto_kast;
    }

    public void setVoto_kast(int voto_kast) {
        this.voto_kast = voto_kast;
    }
}
